package com.example.musiceventsearch;

public class Identifier {
    private String href;
    private String mbid;

    public Identifier() {
    }

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }

    public String getMbid() {
        return mbid;
    }

    public void setMbid(String mbid) {
        this.mbid = mbid;
    }
}
